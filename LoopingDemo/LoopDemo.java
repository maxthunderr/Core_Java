package LoopingDemo;

public class LoopDemo {
    public static void main(String[] args) {
    
        for( int i=1;i<=100;i++)
        {
            if(i%2==0)
            {
                System.out.printf("the number %d is even \n",i);
            }
            else

            {
                System.out.printf("the number %d is odd \n",i);
            }
        }
    }
    
}
